	LEFT JOIN #ANESTYPE	ON #ANESTYPE.LOG_ID = #ALLPROC.LOG_ID
	LEFT JOIN #IMPLANTINFO ON #IMPLANTINFO.IMPLANT_LOG_ID =#ALLPROC.LOG_ID
	LEFT JOIN(
		SELECT
			OR_CASE_ID
		FROM [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].OR_CASE_EQUIP_NEED EQUIP
		WHERE EQUIP.EQUIPMENT_TYPE_C IN (661,1185) --ROBOTIC TYPE
		GROUP BY OR_CASE_ID) ROBOT ON ROBOT.OR_CASE_ID = #ALLPROC.LOG_ID
	OUTER APPLY(
		SELECT
			FR2.FSD_ID
			,FR2.INPATIENT_DATA_ID
			,MEA.RECORDED_TIME
			,MEA.FLT_ID
			,FIRST_VALUE(CASE WHEN FLO_MEAS_ID = '11' THEN MEAS_VALUE ELSE NULL END) OVER (ORDER BY CASE WHEN FLO_MEAS_ID = '11' THEN 0 ELSE 1 END, CASE WHEN MEAS_VALUE IS NULL THEN 1 ELSE 0 END, RECORDED_TIME DESC ) AS 'LASTHEIGHT'  ---- WEIGHT
			,FIRST_VALUE(CASE WHEN FLO_MEAS_ID = '14' THEN MEAS_VALUE ELSE NULL END) OVER (ORDER BY CASE WHEN FLO_MEAS_ID = '14' THEN 0 ELSE 1 END, CASE WHEN MEAS_VALUE IS NULL THEN 1 ELSE 0 END, RECORDED_TIME DESC ) AS 'LASTWEIGHT'  ---- WEIGHT
			,FIRST_VALUE(CASE WHEN FLO_MEAS_ID = '301070' THEN MEAS_VALUE ELSE NULL END) OVER (ORDER BY CASE WHEN FLO_MEAS_ID = '301070' THEN 0 ELSE 1 END, CASE WHEN MEAS_VALUE IS NULL THEN 1 ELSE 0 END, RECORDED_TIME DESC ) AS 'LASTBMI'  ---- BMI
		FROM [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].IP_FLWSHT_REC FR2
			INNER JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].IP_FLWSHT_MEAS MEA  ON MEA.FSD_ID = FR2.FSD_ID
		WHERE FR2.INPATIENT_DATA_ID = #ALLPROC.INPATIENT_DATA_ID
			AND FLO_MEAS_ID IS NOT NULL
			AND FLO_MEAS_ID IN ( '11', '14', '301070')
		ORDER BY MEA.RECORDED_TIME
		OFFSET 0 ROWS FETCH FIRST 1 ROWS ONLY) VITALS


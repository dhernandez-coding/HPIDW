	LEFT JOIN #ALLDX2 ON #ALLDX2.HSP_ACCOUNT_ID = ENC.HSP_ACCOUNT_ID
	LEFT JOIN #ALLPOA ON #ALLPOA.HSP_ACCOUNT_ID = ENC.HSP_ACCOUNT_ID
	LEFT JOIN #ALLCC ON #ALLCC.HSP_ACCOUNT_ID = ENC.HSP_ACCOUNT_ID
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].OR_LOG_TECHNIQUE TECH ON TECH.LOG_ID = #ALLPROC.LOG_ID
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].OR_LOG_TIMING_EVENTS ORSTART ON ORSTART.LOG_ID = #ALLPROC.LOG_ID
																										  AND ORSTART.TIMING_EVENT_C = 700
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].OR_LOG_TIMING_EVENTS OREND ON OREND.LOG_ID = #ALLPROC.LOG_ID
																										  AND OREND.TIMING_EVENT_C = 1200

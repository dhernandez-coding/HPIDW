	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].ZC_PAT_CLASS CLS ON CLS.INTERNAL_ID = #ALLPROC.PAT_TYPE_C
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].ZC_STATE ST	ON ST.INTERNAL_ID = PAT.STATE_C
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].ZC_COUNTRY COUNTRY ON COUNTRY.INTERNAL_ID = PAT.COUNTRY_C
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].ZC_SEX SEX ON SEX.INTERNAL_ID = PAT.SEX_C
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].PATIENT_RACE RACE ON RACE.PAT_ID = PAT.PAT_ID
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].PAT_OR_ADM_LINK LINK ON LINK.LOG_ID = #ALLPROC.LOG_ID
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].PAT_ENC_HSP ENC	ON ENC.PAT_ENC_CSN_ID = LINK.OR_LINK_CSN
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].OR_LOC LOC ON LOC.LOC_ID = #ALLPROC.LOC_ID
	INNER JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].CLARITY_DEP DEP ON DEP.DEPARTMENT_ID = LOC.OR_DEPARTMENT_ID
																								AND DEP.SERV_AREA_ID = 430
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].CLARITY_LOC LOC2 ON LOC2.LOC_ID = DEP.REV_LOC_ID

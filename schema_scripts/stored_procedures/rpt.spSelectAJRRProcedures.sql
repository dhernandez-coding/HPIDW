																										  AND ORINC.TIMING_EVENT_C = 1000
	LEFT JOIN [CLARITYRDBMS.CORP.INTEGRIS-HEALTH.COM].[Clarity].[ORGFILTER].OR_LOG_TIMING_EVENTS ORCLOSE ON ORCLOSE.LOG_ID = #ALLPROC.LOG_ID
																										  AND ORCLOSE.TIMING_EVENT_C = 1100
